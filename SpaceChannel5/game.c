int menuDraw()
{

	int x = 75;
	int y = 30;
	SetColor(10, 5);
	gotoxy(x - 2, y);
	printf("> 게 임 시 작");
	gotoxy(x, y + 1);
	printf("게 임 정 보");
	gotoxy(x, y + 2);
	printf("   종 료  ");

	while (1)
	{
		char c;
		c = _getch();
		int n = keyCheck(c);
		switch (n)
		{
		case UP:
		{
			if (y > 30)
			{
				gotoxy(x - 2, y);
				printf(" ");
				gotoxy(x - 2, --y);
				printf(">");
			}
			break;
		}
		case DOWN:
		{
			if (y < 32)
			{
				gotoxy(x - 2, y);
				printf(" ");
				gotoxy(x - 2, ++y);
				printf(">");
			}
			break;
		}
		case SPACE:
		{
			return y - 30;
		}
		}
	}
	return 0;
}

