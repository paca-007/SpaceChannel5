void gLoop()
{
	clock_t CurTime, OldTime;
	ScreenInit();
	init();
	while (1)
	{
	
		char nKey = getKeyEvent();
		if (nKey == 'q')
			break;

		if (life == 0)
		{
			//every state to -1
			ulalaState = -1;
			meState = -1;
			_correct = -1;
			keyState = -1;
			oxCheck = 0;
			
			while (1)
			{
				char nKey = getKeyEvent();
				if (nKey == 'r')
				{
					restartGame();
					break;
				}
				
				ScreenClear();
				drawGameOver();
				ScreenFlipping();
				Sleep(levelSpeed);
			}
		}
		
		if (level == 3 && _correct == 3)//clear
		{
			//every state to -1
			ulalaState = -1;
			meState = -1;
			_correct = -1;
			keyState = -1;
			oxCheck = 0;

			while (1)
			{
				ScreenClear();
				drawGameClear();
				ScreenFlipping();
			}
		}

		if (musicState == 0)
		{
			PlayMusicSound(1);
			musicState = -1;
		}
			
			
		if (_correct == 3)
		{
			levelUp();
			levelSpeed = 100;
		}
			
		OldTime = clock();
		update(nKey);
		render();
		Sleep(300);
		while (1)
		{
			CurTime = clock();
			if (CurTime - OldTime > 33)
				break;
		}
	}
	
	ScreenRelease();
}

//게임 함수
void restartGame()
{
	//every state to default
	ulalaState = 0;
	meState = 0;
	_correct = 0;
	life = 3;
	keyState = 0;
	oxCheck = 0;
	musicState = 0;
	
}

//각 변수들 초기화 함수
void computerReset()
{
	for (int i = 0; i < 10; i++)
		computer[i] = 0;

}

void playerReset()
{
	for (int i = 0; i < 10; i++)
		player[i] = 0;

}

//레벨 체크 함수
int levelCheck(const int level)
{
	if (level == 1)
		return 3;
	else if (level == 2)
		return 5;
	else if (level == 3)
		return 6;
	else if (level == 4)
		return 8;
	else
		return 10;
}


//입력받은 키보드 체크 함수
int keyCheck(char c)
{
	int keyNum = 0;

	if (c == -32)
	{
		c = _getch();
		switch (c)
		{
		case LEFT:
			keyNum = 75;
			break;
		case RIGHT:
			keyNum = 77;
			break;
		case UP:
			keyNum = 72;
			break;
		case DOWN:
			keyNum = 80;
			break;

		}
	}
	else
	{
		switch (c)
		{
		case SPACE:
			keyNum = 32;
			break;
		}
	}

	return keyNum;
}

//입력받은 값 처리함수
int keyProcess(char key)
{
	int keyNum = 0;
	if (key == -32)
	{
		key = _getch();
		switch (key)
		{
		case LEFT:
			meState = 75;
			keyNum = 75;
			keyState = 75;
			break;
		case RIGHT:
			meState = 77;
			keyNum = 77;
			keyState = 77;
			break;
		case UP:
			meState = 72;
			keyNum = 72;
			keyState = 72;
			break;
		case DOWN:
			meState = 80;
			keyNum = 80;
			keyState = 80;
			break;

		}
	}
	else
	{
		switch (key)
		{
		case SPACE:
			meState = 32;
			keyNum = 32;
			keyState = 32;
			break;
		}
	}
	return keyNum;
}

//컴퓨터 값 생성 함수
void createComputerValue(const int level)
{
	const int num = levelCheck(level);

	computerReset();

	srand((unsigned)time(NULL));

	for (int i = 0; i < num; i++)
	{
		int random = 0;
		random = rand() % 5;
		computer[i] = keyArr[random];
	}

}

//플레이어 값 입력받는 함수
void createPlayerValue(const int level)
{
	const int num = levelCheck(level);
	int key = 0;
	int i = 0;
	playerReset();

	while (i > num)
	{
		char nKey = getKeyEvent();

		keyProcess(nKey);

		key = keyCheck(nKey);
		player[i] = key;
		i++;
	}
	
}

//두 값이 같은지 확인하는 함수
bool isRight(const int level)
{
	const int num = levelCheck(level);

	for (int i = 0; i < num; i++)
	{
		if (computer[i] != player[i])
			return false;
	}
	return true;
}

//레벨업 함수
void levelUp()
{
	level++;
	_correct = 0;

	
}



